export const getServerSideProps: GetServerSideProps = async (ctx) => {
	const session = await getAuthSession(ctx);

	if (!session?.user) {
		return {
			redirect: {
				destination: "/auth/login",
				statusCode: 302,
			},
		};
	}

	return {
		props: { ...session.user },
	};
};

